<h3> Filosofi Warna Sabuk </h3>
<br>
1. Putih
<button class="sembunyi2"> Sembunyikan </button>
<button class="tampil2"> Tampilkan </button>
<p> <img  id="foto2" src="putih.jpg"> </p>
Warna putih melambangkan kesucian, awal atau dasar dari semua warna, dan juga sebagai lambang permulaan. Disini para taekwondoin mempelajari jurus dasar (gibon atau basic) 1 dan 2.
<br>
<br>
<br>
2. Kuning
<button class="sembunyi3"> Sembunyikan </button>
<button class="tampil3"> Tampilkan </button>
<p> <img  id="foto3" src="kuning.jpg"> </p>
Warna kuning melambangkan bumi, disinilah para taekwondoin mulai ditanamkan dasar-dasar TKD dengan kuat serta mempelajari gibon 3. Sebelum naik sabuk hijau, biasanya naik ke sabuk kuning strip hijau terlebih dahulu.
<br>
<br>
<br>
3. Hijau 
<button class="sembunyi4"> Sembunyikan </button>
<button class="tampil4"> Tampilkan </button>
<p> <img  id="foto4" src="hijau.jpg"> </p>
Warna hijau melambangkan hijaunya pepohonan atau kemakmuran, pada waktu inilah dasar TKD mulai ditumbuhkembangkan serta mempelajari taeguk 2. Sebelum naik ke sabuk biru biasanya taekwondoin naik ke sabuk hijau strip biru terlebih dahulu.
<br>
<br>
<br>
4. Biru
<button class="sembunyi5"> Sembunyikan </button>
<button class="tampil5"> Tampilkan </button>
<p> <img  id="foto5" src="biru.jpg"> </p>
Warna biru melambangkan warna birunya langit yang menyelimuti bumi berserta seisinya, yang memberikan arti bahwa kita harus mulai mengetahui apa yang telah kita pelajari disinilah taekwondoin mempelajari taeguk 4. Sebelum naik sabuk merah biasanya taekwondoin naik ke sabuk biru strip merah terlebih dahulu.
<br>
<br>
<br>
5. Merah
<button class="sembunyi6"> Sembunyikan </button>
<button class="tampil6"> Tampilkan </button>
<p> <img  id="foto6" src="merah.jpg"> </p>
Warna merah melambangkan matahari yang berarti bahwa kita mulai menjadi pedoman untuk orang lain serta mengingatkan dan harus dapat mengontrol setiap sikap dan juga tindakan kita disinilah taekwondoin mempelajari taeguk 6. Sebelum naik sabuk hitam, biasanya naik ke sabuk merah strip dua dan juga merah strip satu dahulu. Maksud dari matahari sendiri ialah tingkatan di mana seorang sabuk merah memberi kehangatan atau dalam arti denotasi mulai memberikan ilmu atau bimbingan.
<br>
<br>
<br>
6. Hitam
<button class="sembunyi7"> Sembunyikan </button>
<button class="tampil7"> Tampilkan </button>
<p> <img  id="foto7" src="hitam.jpg"> </p>
Warna terakhir yaitu hitam yang melambangkan akhir, kedalaman, kematangan dalam berlatih serta penguasaan diri kita dari takut dan kegelapan. Hitam mempunyai tahapan dari Dan 1 sampai Dan 9. Warna hitam juaga melambangkan alam semesta atau dunia.
